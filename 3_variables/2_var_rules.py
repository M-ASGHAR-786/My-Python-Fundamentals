# A variable is like a container for storing data values.
# Variable Naming Rules:
# - Variable names must start with a letter or an underscore.
# - Variable names cannot start with a number.
# - Variable names can only contain alpha-numeric characters and underscores (a-z, 0-9, and _).
# - Variable names are case-sensitive (age, Age, and AGE are three different variables).
# - Variable names cannot contain whitespace(empty space).
# - Standard Python function names or keywords cannot be used as variable names(such as print, if, else, and int etc. ).


a = 2                          # Valid
name = "Asghar"                # Valid
